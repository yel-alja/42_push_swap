#include "../include/push_swap.h"

void	pb(t_stack **a, t_stack **b)
{
	t_stack	*tmp;
	t_stack	*tmp2;

	tmp = (*a)->next;
	tmp2 = (*b);
	(*b) = (*a);
	(*b)->next = tmp2;
	(*a) = tmp;
	write(1, "pb\n", 3);
}
