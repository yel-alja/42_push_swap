#include "push_swap.h"

void	pa(t_stack **a, t_stack **b)
{
	t_stack	*tmp;
	t_stack	*tmp2;

	tmp = (*b)->next;
	tmp2 = (*a);
	(*a) = (*b);
	(*a)->next = tmp2;
	(*b) = tmp;
	write(1, "pa\n", 3);
}
